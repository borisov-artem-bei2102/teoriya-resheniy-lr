# –¢—Ä–µ–±—É–µ—Ç—Å—è –Ω–∞–π—Ç–∏ –æ–ø—Ç–∏–º–∞–ª—å–Ω—É—é —Å—Ç—Ä–∞—Ç–µ–≥–∏—é –¥–ª—è  ùëÅ=‚àû  –º–µ—Ç–æ–¥–æ–º –∏—Ç–µ—Ä–∞—Ü–∏–π –ø–æ —Å—Ç—Ä–∞—Ç–µ–≥–∏—è–º.

from sympy import symbols, solve
from pandas import DataFrame

E = symbols('E')
F = symbols('F')


def solve_eval(eq1, eq2):
    solving_result = solve([eq1, eq2], [E, F], dict=True)[0]
    result = dict((k, float(v)) for (k, v) in solving_result.items())
    print('E = {:.2f}'.format(result[E]))
    print('F = {:.2f}'.format(result[F]))
    return result


eval1 = solve_eval(E + 0.3 * F - 10, E + 0.7 * F - 12)

improve_table = DataFrame({
    'S': range(1, 3),
    'X1': [1, 0],
    'X2': [0, -10 + 0.5 * eval1[F]],
    'X3': [0, -12 + 0.7 * eval1[F]]
})
improve_table.set_index('S')

print(improve_table)
