# (—Å–æ—Å—Ç–æ—è–Ω–∏–µ 2). –ï—Å–ª–∏ –º–∞—à–∏–Ω–∞ –Ω–∞ –Ω–µ–∫–æ—Ç–æ—Ä–æ–º —à–∞–≥–µ –ø—Ä–æ—Ä–∞–±–æ—Ç–∞–ª–∞ –Ω–µ–ø—Ä–µ—Ä—ã–≤–Ω–æ 1 —á–∞—Å, —Ç–æ –¥–æ—Ö–æ–¥ —Ä–∞–≤–µ–Ω 3 —Ä—É–±–ª—è–º.
# –ü—Ä–∏ —ç—Ç–æ–º –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –æ—Å—Ç–∞—Ç—å—Å—è –Ω–∞ —Å–ª–µ–¥—É—é—â–µ–º —à–∞–≥–µ –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–∏ 1 —Ä–∞–≤–Ω–∞ 0.7,
# –∞ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –ø–µ—Ä–µ–π—Ç–∏ –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–µ 2 —Ä–∞–≤–Ω–∞ 0.3. –ï—Å–ª–∏ –º–∞—à–∏–Ω–∞ –æ—Ç–∫–∞–∑–∞–ª–∞ –Ω–∞ –Ω–µ–∫–æ—Ç–æ—Ä–æ–º —à–∞–≥–µ,
# —Ç–æ –µ–µ –º–æ–∂–Ω–æ –æ—Ç—Ä–µ–º–æ–Ω—Ç–∏—Ä–æ–≤–∞—Ç—å –¥–≤—É–º—è —Å–ø–æ—Å–æ–±–∞–º–∏. –ü–µ—Ä–≤—ã–π —è–≤–ª—è–µ—Ç—Å—è —É—Å–∫–æ—Ä–µ–Ω–Ω—ã–º,
# —Ç—Ä–µ–±—É–µ—Ç –∑–∞—Ç—Ä–∞—Ç –≤ 2 —Ä—É–±–ª—è (–¥–æ—Ö–æ–¥ —Ä–∞–≤–µ–Ω -2) –∏ –æ–±–µ—Å–ø–µ—á–∏–≤–∞–µ—Ç –ø–µ—Ä–µ—Ö–æ–¥ –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–µ 1 —Å –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å—é 0.6.
# –í—Ç–æ—Ä–æ–π, –æ–±—ã—á–Ω—ã–π —Å–ø–æ—Å–æ–± —Ç—Ä–µ–±—É–µ—Ç –∑–∞—Ç—Ä–∞—Ç –≤ 1 —Ä—É–±–ª—å –∏ –æ–±–µ—Å–ø–µ—á–∏–≤–∞–µ—Ç –ø–µ—Ä–µ—Ö–æ–¥ –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–µ 1 —Å –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å—é 0.4.
